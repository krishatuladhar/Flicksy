import Users from "../models/userModel.js";

export const createPost = async (req, res, next) => {
  try {
    const { userId } = req.body.user;
    const { description, image } = req.body;

    console.log("Creating post with:", { userId, description, image });

    if (!description) {
      return res.status(400).json({
        status: "failed",
        message: "You must provide a description"
      });
    }

    const post = await Posts.create({
      userId,
      description,
      image: image || "", // Handle case where image is undefined
    });

    console.log("Post created successfully:", post);

    res.status(200).json({
      status: "success",
      message: "Post created successfully",
      data: post,
    });
  } catch (error) {
    console.error("Error creating post:", error);
    res.status(500).json({ 
      status: "failed",
      message: error.message || "Failed to create post"
    });
  }
};

export const getPosts = async (req, res, next) => {
  try {
    const { userId } = req.user || {};
    const { search } = req.query || {};

    let posts = await Posts.find(search ? {
      description: { $regex: search, $options: "i" }
    } : {})
    .populate({
      path: "userId",
      select: "firstName lastName location profileUrl -password",
    })
    .sort({ _id: -1 });

    if (userId) {
      const user = await Users.findById(userId);
      const friends = user?.friends?.toString().split(",") ?? [];
      friends.push(userId);

      const friendsPosts = posts?.filter((post) => {
        return friends.includes(post?.userId?._id.toString());
      });

      const otherPosts = posts?.filter(
        (post) => !friends.includes(post?.userId?._id.toString())
      );

      posts = friendsPosts?.length > 0 
        ? (search ? friendsPosts : [...friendsPosts, ...otherPosts])
        : posts;
    }

    res.status(200).json({
      success: true,
      message: "Posts fetched successfully",
      data: posts,
    });
  } catch (error) {
    console.log(error);
    res.status(404).json({ message: error.message });
  }
};

